def mm_to_twips(mm: float) -> int:
    return int(round(mm * 56.6929133858))


def pt_to_half_points(pt: int) -> int:
    return pt * 2


def xml_text(text: str) -> str:
    return escape(text).replace("\n", "</w:t><w:br/><w:t>")


def p(text: str, size=22, bold=False, align="right", spacing_after=80):
    jc = {"right": "right", "center": "center", "justify": "both"}[align]
    bold_xml = "<w:b/>" if bold else ""
    return f"""
    <w:p>
      <w:pPr>
        <w:bidi/>
        <w:jc w:val="{jc}"/>
        <w:spacing w:after="{spacing_after}" w:line="320" w:lineRule="auto"/>
      </w:pPr>
      <w:r>
        <w:rPr>
          <w:rtl/>
          <w:lang w:val="ar-TN" w:bidi="ar-TN"/>
          <w:rFonts w:ascii="Arial" w:hAnsi="Arial" w:cs="Arial"/>
          <w:sz w:val="{pt_to_half_points(size)}"/>
          <w:szCs w:val="{pt_to_half_points(size)}"/>
          {bold_xml}
        </w:rPr>
        <w:t xml:space="preserve">{xml_text(text)}</w:t>
      </w:r>
    </w:p>
    """


def cell_paragraph(text: str, size=20, bold=False, align="right"):
    jc = {"right": "right", "center": "center", "justify": "both"}[align]
    bold_xml = "<w:b/>" if bold else ""
    return f"""
    <w:p>
      <w:pPr>
        <w:bidi/>
        <w:jc w:val="{jc}"/>
        <w:spacing w:after="40" w:line="300" w:lineRule="auto"/>
      </w:pPr>
      <w:r>
        <w:rPr>
          <w:rtl/>
          <w:lang w:val="ar-TN" w:bidi="ar-TN"/>
          <w:rFonts w:ascii="Arial" w:hAnsi="Arial" w:cs="Arial"/>
          <w:sz w:val="{pt_to_half_points(size)}"/>
          <w:szCs w:val="{pt_to_half_points(size)}"/>
          {bold_xml}
        </w:rPr>
        <w:t xml:space="preserve">{xml_text(text)}</w:t>
      </w:r>
    </w:p>
    """


def make_cell(text: str, width: int, header=False):
    shade = '<w:shd w:val="clear" w:color="auto" w:fill="D9EAD3"/>' if header else ""
    return f"""
    <w:tc>
      <w:tcPr>
        <w:tcW w:w="{width}" w:type="dxa"/>
        {shade}
        <w:vAlign w:val="center"/>
      </w:tcPr>
      {cell_paragraph(text, size=10 if not header else 11, bold=header, align="center" if header else "right")}
    </w:tc>
    """


def make_row(cells, widths, header=False):
    xml_cells = "".join(make_cell(text, width, header=header) for text, width in zip(cells, widths))
    return f"<w:tr>{xml_cells}</w:tr>"


def make_table(rows, widths):
    tbl_grid = "".join(f'<w:gridCol w:w="{w}"/>' for w in widths)
    body = [make_row(["القائمة / القسم", "الخيار", "الوصف التفصيلي والأثر"], widths, header=True)]
    for row in rows:
        body.append(make_row(row, widths))
    return f"""
    <w:tbl>
      <w:tblPr>
        <w:tblW w:w="0" w:type="auto"/>
        <w:jc w:val="center"/>
        <w:tblBorders>
          <w:top w:val="single" w:sz="8" w:space="0" w:color="888888"/>
          <w:left w:val="single" w:sz="8" w:space="0" w:color="888888"/>
          <w:bottom w:val="single" w:sz="8" w:space="0" w:color="888888"/>
          <w:right w:val="single" w:sz="8" w:space="0" w:color="888888"/>
          <w:insideH w:val="single" w:sz="6" w:space="0" w:color="BBBBBB"/>
          <w:insideV w:val="single" w:sz="6" w:space="0" w:color="BBBBBB"/>
        </w:tblBorders>
      </w:tblPr>
      <w:tblGrid>{tbl_grid}</w:tblGrid>
      {''.join(body)}
    </w:tbl>
    <w:p/>
    """


